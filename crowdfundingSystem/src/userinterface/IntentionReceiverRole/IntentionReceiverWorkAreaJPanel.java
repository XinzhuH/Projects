     public void populateRequestTable(){
           
        DefaultTableModel model = (DefaultTableModel) workRequestJTable1.getModel();   
        model.setRowCount(0);
        if(organization instanceof CommercialOrganization){
        for (WorkRequest request : network.getIntentionList().getWorkRequestList()){
            
            if(request.getStatus().equals("Sent")&&request.getSector().equals(Organization.Type.Commercial)){
//            System.out.println(request+" "+request.getStatus());
            Object[] row = new Object[4];
            row[0] = request;
            row[1] = Double.toString(request.getProvidedFunds()) ;
            row[2] = request.getSector();
            row[3] = request.getStatus();            
            model.addRow(row);}
        }
        }else if(organization instanceof CharityOrganization){
            for (WorkRequest request : network.getIntentionList().getWorkRequestList()){
            
            if(request.getStatus().equals("Sent")&&request.getSector().equals(Organization.Type.Charity)){
               
            Object[] row = new Object[4];
            row[0] = request;
            row[1] = Double.toString(request.getProvidedFunds()) ;
            row[2] = request.getSector();
            row[3] = request.getStatus();
            
            model.addRow(row);}
        }
        }
        }
